#	include <unistd.h>

int	ft_str_is_numeric(char *str)
{
	int ret;
	int i;

	ret = 1;
	i = 0;
	while (str[i] != 0)
	{
		if (str[i] < '0' || '9' < str[i])
			ret = 0;
		i++;
	}
	return (ret);
}
