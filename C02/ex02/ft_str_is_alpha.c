#	include <unistd.h>

int	ft_str_is_alpha(char *str)
{
	int ret;
	int i;

	ret = 1;
	i = 0;
	while (str[i] != 0)
	{
		if (str[i] < 'A' || ('Z' < str[i] && str[i] < 'a') || 'z' < str[i])
			ret = 0;
		i++;
	}
	return (ret);
}
