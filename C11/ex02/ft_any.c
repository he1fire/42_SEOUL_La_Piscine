int	ft_any(char **tab, int (*f)(char*))
{
	int ret;
	int i;

	ret = 0;
	i = 0;
	while (tab[i] != 0)
	{
		if (f(tab[i]) != 0)
			ret = 1;
		i++;
	}
	return (ret);
}
